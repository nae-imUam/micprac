.model small
.386
.data
DATA1 db 00H
DATA2 db 00H
PROD1 db ?
PROD2 db ?
msg db 10,13,"Enter the first no.:: $"
msg1 db 10,13,"Enter the second no.:: $"
msg2 db 10,13,"The product(in hexadecimal) is :: $"


.code
.startup
MOV AH,09
MOV DX,OFFSET msg
INT 21H

MOV BL,0
MOV CX,2
AGAIN: MOV AH,01 ;1ST NO. ENTERED
INT 21H

CMP AL,'A'
JGE L5
SUB AL,30H
JMP L6
L5: SUB AL,37H

L6: SHL BL,4
ADD BL,AL
LOOP AGAIN
MOV DATA1,BL


MOV AH,09
MOV DX,OFFSET msg1
INT 21H
MOV BL,0
MOV CX,2
AGAIN1:MOV AH,01 ;2nd NO. ENTERED
INT 21H
CMP AL,'A'
JGE L7
SUB AL,30H
JMP L8
L7: SUB AL,37H
L8: SHL BL,4
ADD BL,AL
LOOP AGAIN1

MOV DATA2,BL

MOV BL, 0
MOV DL,0
MOV AL,0
MOV AL,DATA1
MOV BL,DATA2


MUL BL

MOV PROD1,AH
MOV PROD2,AL

MOV AH,09
MOV DX,OFFSET msg2
INT 21H

MOV BL,PROD1
MOV CX,2
AGAIN2: ROL BL,4
MOV DL,BL
AND DL,0FH ; to o/p the result
CMP DL,9
JBE L1
ADD DL,37H
MOV AH,02
INT 21H
JMP L2
L1: ADD DL,30H
MOV AH,02
INT 21H
L2: LOOP AGAIN2

MOV BL,PROD2
MOV CX,2
AGAIN3: ROL BL,4
MOV DL,BL
AND DL,0FH ; to o/p the result
CMP DL,9
JBE L3
ADD DL,37H
MOV AH,02
INT 21H
JMP L4
L3: ADD DL,30H
MOV AH,02
INT 21H
L4: LOOP AGAIN3
.EXIT
END