.MODEL SMALL
.STACK 100H
.386
.DATA
INP1 DD 00000000H
MSG1 DB 10,13, "ENTER THE FIRST NUMBER (8 DIGIT)::> $"
MSG2 DB 10,13, "ENTER THE SECOND NUMBER (8 DIGIT)::> $"
ADD1 DB 10,13, "RESULT OF ADDITION IS::> $"

.CODE
.STARTUP
MAIN PROC
			MOV AH,09		;INP1
			MOV DX, OFFSET MSG1
			INT 21H

			MOV CX,8
			REPEAT1:MOV AH,01
			INT 21H
			CMP AL, 'A'
			JGE G
			SUB AL,30H
			JMP C
			G: SUB AL,37H
			C: SHL EBX, 4   ;don't modify value of CL it is a counter
			MOV AH, 00

			ADD EBX,EAX
			LOOP REPEAT1

			MOV INP1,EBX

			MOV AH,09			;INP2
			MOV DX, OFFSET MSG2
			INT 21H

			MOV CX,8
			REPEAT2:MOV AH,01
			INT 21H 
			CMP AL, 'A'
			JGE GG
			SUB AL,30H
			JMP CC
			GG: SUB AL,37H
			CC:SHL BL,4 ;same as inp1 
			MOV AH, 00

			ADD EBX,EAX
			LOOP REPEAT2

			ADD EBX, INP1

			MOV AH,09		
			MOV DX, OFFSET ADD1
			INT 21H

			MOV DL,00
			MOV CX,8

			REPEAT3: ROL EBX, 4		; ROL TO GET THE CORRECT OUTPUT
			MOV DL,BL
			AND DL, 0FH
			CMP DL, 09
			JG ADD2
			ADD DL,30H
			JMP FIN

			ADD2:ADD DL, 37H

			FIN: MOV AH,02
			INT 21H

			LOOP REPEAT3

MOV AH,4CH
INT 21H			

MAIN ENDP
END
.EXIT