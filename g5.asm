.model small
.386
.data
msg db 10,13,"Enter the no.:: $"

msg2 db 10,13,"The number you entered is :: $"

.code
.startup
MOV AH,09
MOV DX,OFFSET msg
INT 21H

MOV BL,0
MOV CX,2
AGAIN: MOV AH,01 ;1ST NO. ENTERED
INT 21H
CMP AL,'A'
JGE L5
SUB AL,30H
JMP L6
L5: SUB AL,37H
L6: SHL BL,4
ADD BL,AL
LOOP AGAIN




MOV AH,09
MOV DX,OFFSET msg2
INT 21H

MOV CX,2
AGAIN2: ROL BL,4
MOV DL,BL
AND DL,0FH
CMP DL,09
JG L1 ; to o/p given no.
ADD DL,30H
JMP PRINT
L1: ADD DL,37H
PRINT: MOV AH,02
INT 21H
LOOP AGAIN2
.EXIT
END